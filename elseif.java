public class elseif {
    public static void main(String args[]) {
        int x = 30;
        if (x == 10) {
            System.out.print("x is equal to 10");
        } else if (x == 20) {
            System.out.print("x is equal to 20");
        } else if (x == 30) {
            System.out.print("x is equal to 30");
        } else {
            System.out.println("x is not equal to 12,20 and 30");
        }
    }
}