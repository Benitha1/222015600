public class ifstatement {
    // this class demonstrates how to use the if statement in java
    public static void main(String args[]) {
        //declare two variables
        int a = 10;
        int b = 15;
        
        if (a > b) { // compare two variables
            System.out.println("a is greater"); // print a message if a is greater then b
        } else {
            System.out.println(" b is greater"); // print a message if b is greater then b or equal to a
        }

    }

}
