// this demonstrates the for loop in java
public class forloop { 
    public static void main(String args[]) { // the main method
        for (int x = 10; x < 20; x = x + 1) { // declaring a variable as integer x to store the current loop iteration
            System.out.println("value of x :" + x); // print the value of the current loop iteration

        }

    }

}
