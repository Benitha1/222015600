// class to define a break statement
public class breakstatement {
    public static void main(String args[]) { // mandatory main method
        for (int j = 10; j <= 20; j++) { // declaration of a variable as integer j and iterate from 10 to 20

            if (j == 15) { // if j is equal to 15 , break out the loop
                
                break;
                
            }
            System.out.println(j); // print the value of j

        }

    }

}
